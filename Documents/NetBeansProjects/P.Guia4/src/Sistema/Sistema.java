    private void B_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_ActualizarActionPerformed
        // Boton Actualizar
        try {
            String fecha = new SimpleDateFormat("dd/MM/yyyy").format(Fecha_Asignacion.getDate());
            PreparedStatement pst = con.prepareStatement("UPDATE elementos SET Tipo='" + Tipo.getText() + "',Descripcion='"
                    + Descripcion.getText() + "',CC_Funcionario='" + CC_Funcionario.getText() + "',Nombre_Funcionario='"
                    + Nombre_Funcionario.getText() + "',Dependencia='" + Dependencia.getText() + "',Fecha_Asignacion='"
                    + fecha + "' WHERE serie='" + Serie.getText() + "'");
            int a = pst.executeUpdate();
            if (a > 0) {
                JOptionPane.showMessageDialog(null, "Se Modifico de Forma Satisfactoria");
            } else {
                JOptionPane.showMessageDialog(null, "No se Pudo Modificar el Registro");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Algo esta fallando: \n" + e.getMessage());
        }
    }//GEN-LAST:event_B_ActualizarActionPerformed

    private void B_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_EliminarActionPerformed
        // Boton Eliminar
        try {
            PreparedStatement pst = con.prepareStatement("DELETE FROM elementos WHERE serie='" + Serie.getText() + "'");
            int a = pst.executeUpdate();
            if (a > 0) {
                JOptionPane.showMessageDialog(null, "Se Elimino de Forma Satisfactoria");
            } else {
                JOptionPane.showMessageDialog(null, "No se Pudo Eliminar el Registro");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Algo esta fallando: \n" + e.getMessage());
        }

    }//GEN-LAST:event_B_EliminarActionPerformed

