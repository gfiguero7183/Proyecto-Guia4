    void Consulta(String Valor) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("N° Serie");
        modelo.addColumn("Tipo");
        modelo.addColumn("Descripcion Elemento");
        modelo.addColumn("CC. Funcionario");
        modelo.addColumn("Nombre Funcionario");
        modelo.addColumn("Dependencia");
        modelo.addColumn("Fecha");
        Reporte.show();
        Reporte.setModel(modelo);
        String sql = "";

        if (Valor.equals("")) {
            sql = "SELECT * FROM elementos";
        } else {
            sql = "SELECT * FROM elementos WHERE Tipo='" + Valor + "'";
        }
        String[] datos = new String[7];
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString("Serie");
                datos[1] = rs.getString("Tipo");
                datos[2] = rs.getString("Descripcion");
                datos[3] = rs.getString("CC_Funcionario");
                datos[4] = rs.getString("Nombre_Funcionario");
                datos[5] = rs.getString("Dependencia");
                datos[6] = rs.getString("Fecha_Asignacion");
                modelo.addRow(datos);
            }
            Reporte.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(Sistema.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Algo esta fallando: \n" + ex.getMessage());
        }

    }

