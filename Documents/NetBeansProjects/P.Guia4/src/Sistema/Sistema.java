    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        // Boton Guardar

        try {
            PreparedStatement pst = con.prepareStatement("INSERT INTO elementos (Serie,Tipo,Descripcion,CC_Funcionario,Nombre_Funcionario,Dependencia,Fecha_Asignacion) VALUES (?,?,?,?,?,?,?)");
            pst.setString(1, Serie.getText());
            pst.setString(2, Tipo.getText());
            pst.setString(3, Descripcion.getText());
            pst.setString(4, CC_Funcionario.getText());
            pst.setString(5, Nombre_Funcionario.getText());
            pst.setString(6, Dependencia.getText());
            String fecha = new SimpleDateFormat("dd/MM/yyyy").format(Fecha_Asignacion.getDate());
            pst.setString(7, fecha);

            int a = pst.executeUpdate();
            if (a > 0) {
                JOptionPane.showMessageDialog(null, "Creado de Forma Satisfactoria");
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo Crear el registro");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Algo esta fallando: \n" + e.getMessage());
        }
    }//GEN-LAST:event_GuardarActionPerformed

